
package modelo;

/**
* Esta enumeración representa la capacidad que tiene cada mesa.
* Las capacidades posibles son:
* dos: significa que una mesa tiene 2 sillas
* cuatro: significa que una mesa tiene 4 sillas
* seis: significa que una mesa tiene 6 sillas
* ocho: significa que una mesa tiene 8 sillas
* diez: significa que una mesa tiene 10 sillas
* @author Marcos Ramon Caraballo, Angelina María Vialle,
* @version 27/10/2024 
*/
public enum Capacidad {
    dos, cuatro, seis, ocho, diez
}
